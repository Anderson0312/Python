print('Digite dois numeros para compararmos qual é maior! ')
n1 = int(input('Digite o primeiro número: '))
n2 = int(input('Digite o segundo número: '))
if n1 > n2:
    print('O primero valor é maior que o segundo.')
elif n2 > n1:
    print('O segundo valor é maior que o primerio.')
else:
    print('Os dois valores são iguais.')